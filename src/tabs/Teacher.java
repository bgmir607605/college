    String lName, fName, mName;
    void addTeacher(){
        lName = lNameTeacher.getText();
        fName = fNameTeacher.getText();
        mName = mNameTeacher.getText();
        if (Checks.notEmpTeacher(lName, fName, mName)){    
        try {
                new DB().ins("INSERT INTO teachers (fName, mName, lName) values('"+ fName + "', '"+ mName + "', '" + lName + "');");
            } catch (SQLException ex) {
                Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
            }
        fNameTeacher.setText(null);
        mNameTeacher.setText(null);
        lNameTeacher.setText(null);
        refTab();
        }
    }
    void delTeacher(){
                /**
         * Ошибка при удалении записи, имеющей внешние связи в БД
         * 
         * 
         */ 
        int sr = jTable1.getSelectedRow();
        lName = (String) jTable1.getValueAt(sr, 0);
        fName = (String) jTable1.getValueAt(sr, 1);
        mName = (String) jTable1.getValueAt(sr, 2);
        try {
            new DB().del("teachers", "lName = '" + lName + "' and fName = '" + fName + "' and mName = '" + mName + "'");
        } catch (SQLException ex) {
            Logger.getLogger(Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        refTab();
    }
    void editTeacher(){
        EditForm teacher = new EditForm(this,
            (String) jTable1.getValueAt(jTable1.getSelectedRow(), 0),
            (String) jTable1.getValueAt(jTable1.getSelectedRow(), 1),
            (String) jTable1.getValueAt(jTable1.getSelectedRow(), 2));
    }
    void refTab(){
        jTable1.setModel(new MyTable("teachers"));
    }
